a = 2 + 3
print(a)

b = 2 - 3
print(b)

c = 2 * 3
print(c)

d = 2 / 3 #Devuelve un float, independientemente si el resultado es entero
print(d)
print(type(d)) #Type sirve para identificar el tipo de dato (simple o complejo)

e = 2 ** 3 #Exponente
print(e)

f = 2 // 3 #División entera, redondea hacia abajo ignorando la cantidad decimal 
print(f)

g = 2 % 3 #Módulo o resto
print(g)