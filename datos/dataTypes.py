"""Es necesario recordar que al ser un lenguaje de tipado dinámico, NO es necesario hacer esto c/vez que introducimos un dato, esto se hace para 
cambiar el tipo de dato según necesitemos."""

#Enteros
a = int(1) 
#1

#Reales (c/decimales)
b = float(1) 
#1.0

#Booleano (Si o no, 0 ó 1, True or False)
c = bool(1) 
#True

#String
d = str(1) 
#'1'

print(
a,type(a),
b,type(b),
c,type(c),
d,type(d)
)

